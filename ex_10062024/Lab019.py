# Escape Sequence
print("Hello \"World\" World")   #Hello "World" World
print('I\'m from India')      #I'm from India
print("Hello\nWorld")
print("Hello\tWorld")   #tab space
print("Hello\rWorld")   #last word
print("Hello\bWorld")   #do backspace
print("Hello\fWorld")   #HelloWorld
print("Hello\\World")    #Hello\World


